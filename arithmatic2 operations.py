 # Arithmatic operation of numpy
import numpy as np
x1=np.array([1,2,4,5,6])
x2=np.array([1,1,1,1,1])

x3=np.add(x1,x2)
print(x3)

x3=np.subtract(x1,x2)
print(x3)

x3=np.divide(x1,x2)
print(x3)

x3=np.multiply(x1,x2)
print(x3)

x3=np.mod(x1,x2)
print(x3)

x3=np.power(x1,x2)
print(x3)

x3=np.reciprocal(x1,x2)
print(x3)


